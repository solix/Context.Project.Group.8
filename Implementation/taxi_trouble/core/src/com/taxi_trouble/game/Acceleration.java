package com.taxi_trouble.game;

/**An enumeration of the ways in which a taxi can accelerate.
 * 
 * @author Computer Games Project Group 8
 *
 */
public enum Acceleration {
    /**
     * Accelerate / speed up the taxi.
     */
    ACC_ACCELERATE,
    
    /**
     * Steering right
     */
    ACC_BRAKE,
    
    /**
     * Steering in no direction
     */
    ACC_NONE;
}
